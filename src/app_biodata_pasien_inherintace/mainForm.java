    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JOptionPane.showMessageDialog(null, "Data Berhasil Di Simpan");
        String jk;
        if (jkPria.isSelected()) {
            jk = "Pria";
        } else{
            jk = "Wanita";
        }
        sessionBiodata.setno_rm(txtNoRM.getText());
        sessionBiodata.setnama_pasien(txtNamaPasien.getText());
        sessionBiodata.setalamat(txtAlamat.getText());
        sessionBiodata.setjenis_kelamin(jk);
        sessionBiodata.setpoli_spesialis(cmbPoliSpesialis.getSelectedItem().toString());
        sessionBiodata.setgol_darah(cmbGolonganDarah.getSelectedItem().toString());
    }//GEN-LAST:event_jButton1ActionPerformed

